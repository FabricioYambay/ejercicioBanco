    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        ISucursal sucursalDao = new SucursalImpl();
        Sucursal sucursal = new Sucursal();
        if (sucursal != null) {
            int respuesta = JOptionPane.YES_NO_CANCEL_OPTION;
            sucursal.setCodigoS(Integer.parseInt(txtCodigo.getText()));
            respuesta = JOptionPane.showConfirmDialog(null, "Esta Seguro que desea Eliminar el Sucursal "
                    + sucursal.getCodigoS() + " ?" + respuesta);
            try {
                if (respuesta == JOptionPane.YES_OPTION) {
                    int numFilasAfectadas = sucursalDao.eliminar(sucursal);
                    if (numFilasAfectadas > 0) {
                        JOptionPane.showMessageDialog(this, "Se Elimino!!");
                    } else {
                        JOptionPane.showMessageDialog(this, "No se pudo eliminar contiene llave foranea!!");
                    }
                }
            } catch (Exception e) {

                JOptionPane.showMessageDialog(this, "No se pudo Eliminar!!");

            }

        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        ISucursal sucursalDao = new SucursalImpl();
        try {
            sucursalS = sucursalDao.obtener(Integer.parseInt(txtCodigo.getText()));
            if (sucursalS != null) {
                txtCiudad.setText(sucursalS.getCiudad());
                txtEmail.setText(sucursalS.getEmail());
                txtDireccion.setText(sucursalS.getDireccion());
                txtTelefono.setText(sucursalS.getTelefono());

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al buscar: " + e.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);

        }
        txtCodigo.setEnabled(false);

    }//GEN-LAST:event_btnBuscarActionPerformed
    public void btnModificarActionListener(ActionEvent e) {
         
        Sucursal sucursal = new Sucursal();
        ISucursal sucursalDao = new SucursalImpl();
        sucursal.setCodigoS(Integer.parseInt(txtCodigo.getText()));
        sucursal.setCiudad(txtCiudad.getText());
        sucursal.setDireccion(txtDireccion.getText());
        sucursal.setDireccion(txtTelefono.getText());
        sucursal.setDireccion(txtEmail.getText());

        try {
            if (sucursalDao.insertar(sucursal) > 0) {
                JOptionPane.showMessageDialog(this, "Registrado correctamente!!",
                        "Transacci√≥n correcta", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Error desconocido: ",
                        "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error desconocido: " + ex.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
    
    }
